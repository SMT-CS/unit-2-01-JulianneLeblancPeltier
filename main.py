import math


def main() -> None:
    """
    This function calculates the circumference of a circle with a diameter of 42 mm
    """

    print("If a circle has a diameter of 42 mm:\n")
    print(f"Circumference is: {(math.pi * 42):.5f} mm")

    print("\nDone! :)")


if __name__ == "__main__":
    main()
